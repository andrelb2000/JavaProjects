    public Viagem obterViagem(){
        String destino = destinojTextField1.getText();
        String origem  = origemjTextField2.getText();
        double preco = Double.parseDouble(precojTextField3.getText());
        int    nrdias = Integer.parseInt(nrDiasjTextField4.getText());
        int milhas    = Integer.parseInt(milhasjTextField5.getText());
        Viagem v = new Viagem(destino, origem, preco, nrdias, milhas);
        
        return v;
    }
